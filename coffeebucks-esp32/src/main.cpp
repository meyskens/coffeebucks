int bootCount = 0;

void setClock() {
  configTime(0, 0, "pool.ntp.org", "time.nist.gov");

  Serial.print(F("Waiting for NTP time sync: "));
  time_t nowSecs = time(nullptr);
  while (nowSecs < 8 * 3600 * 2) {
    delay(500);
    Serial.print(F("."));
    yield();
    nowSecs = time(nullptr);
  }

  Serial.println();
  struct tm timeinfo;
  gmtime_r(&nowSecs, &timeinfo);
  Serial.print(F("Current time: "));
  Serial.print(asctime(&timeinfo));
}

void buyCoffee() {
  WiFiClientSecure *client = new WiFiClientSecure;
  if (!client) {
    Serial.println("ERROR: WiFiClientSecure error");
    return;
  }
  client -> setCACert(rootCACertificate);

  HTTPClient http;
  http.begin(serverName);

  // Specify content-type header
  http.addHeader("Content-Type", "application/x-www-form-urlencoded");
  // Data to send with HTTP POST
  String httpRequestData = "token=" + String(token);
  // Send HTTP POST request
  int httpResponseCode = http.POST(httpRequestData);

  Serial.print("HTTP Response code: ");
  Serial.println(httpResponseCode);

  // Free resources
  http.end();
  Serial.println("Waiting 10s before the next coffee...");
  delay(10000);
}

void setup() {
  Serial.begin(9600);

  WiFi.begin(ssid, password);
  Serial.println("Connecting");
  while (WiFi.status() != WL_CONNECTED)
  {
    delay(500);
    Serial.print(WiFi.status());
  }
  Serial.println("");
  Serial.print("Connected to WiFi network with IP Address: ");
  Serial.println(WiFi.localIP());

  setClock();

  if (esp_sleep_get_wakeup_cause() == ESP_SLEEP_WAKEUP_EXT0) {
    buyCoffee();
  }

  Serial.println("Goodnight!");
  pinMode(GPIO_NUM_26, INPUT_PULLUP);
  esp_sleep_enable_ext0_wakeup(GPIO_NUM_26, LOW);
  esp_deep_sleep_start();
}

